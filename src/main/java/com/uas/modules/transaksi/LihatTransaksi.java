    private void transaksiTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_transaksiTableMouseClicked
        showStatus(true);

        // set label transaksi
        this.namaLabel.setText(getValueFromTable(1));
        this.kurisLabel.setText(getValueFromTable(2));
        this.metodeLabel.setText(getValueFromTable(4));
        this.totalLabel.setText(getValueFromTable(5));
        this.alamatLabel.setText(getValueFromTable(3));

        StatusPemesanan pemesanan = this.statusPemesananRepository.findByIdTransaksi(getValueFromTable(0));

        // set label status pemesanan
        idTransaksiLabel.setText(pemesanan.getIdTransaksi());
        statusPembayaranLabel.setText(pemesanan.getStatusPembayaran());
        statusPemesananLabel.setText(pemesanan.getStatusPemesanan());

        if (statusPemesananLabel.getText().equals("Belum diproses")) {
            barangBTN.setEnabled(false);
        } else {
            barangBTN.setEnabled(true);
        }

    }//GEN-LAST:event_transaksiTableMouseClicked

    private void barangBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barangBTNActionPerformed
        statusPemesananRepository.delete(getValueFromTable(0));
        transaksiRepository.delete(Integer.parseInt(getValueFromTable(0)));

        JOptionPane.showMessageDialog(null, "Barang telah sampai!!!, data akan dihapus dari transaksi.");
        showDataTables();
        showStatus(false);
    }//GEN-LAST:event_barangBTNActionPerformed

    private String getValueFromTable(int column) {
        return transaksiTable.getValueAt(transaksiTable.getSelectedRow(), column).toString();
    }

