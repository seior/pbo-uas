    public void showTables() {
        Object[] tableNames = {
                "id",
                "id laptop",
                "jasa pengiriman",
                "alamat",
                "pembayaran",
                "total bayar",
        };

        tableModel = new DefaultTableModel(null, tableNames);

        transaksiTable.setModel(tableModel);
    }

    public void showDataTables() {
        tableModel.getDataVector().removeAllElements();
        tableModel.fireTableDataChanged();

        List<Transaksi> result = transaksiRepository.findAll(this.username);

        result.forEach(transaksi -> {
            Object[] data = {
                    transaksi.getId(),
                    transaksi.getIdLaptop(),
                    transaksi.getJasaPengiriman(),
                    transaksi.getAlamat(),
                    transaksi.getPembayaran(),
                    transaksi.getTotalBayar()
            };

            tableModel.addRow(data);
        });
    }

